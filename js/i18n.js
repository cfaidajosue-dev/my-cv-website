// ===== TYPEWRITER PHRASES PER LANGUAGE =====
const typewriterPhrases = {
  en: ['Software Developer', 'Web Developer', 'Problem Solver', 'S5 Student 🇷🇼'],
  fr: ['Développeur Logiciel', 'Développeur Web', 'Résolveur de Problèmes', 'Étudiant S5 🇷🇼'],
  rw: ['Umukora Software', 'Umukora Urubuga', 'Ushoboye Gukemura Ibibazo', 'Umunyeshuri S5 🇷🇼']
};

// ===== APPLY TRANSLATIONS =====
function applyTranslation(lang) {
  const t = translations[lang];
  if (!t) return;

  // Translate all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });

  // Translate placeholders (contact form + chat input)
  const nameInput    = document.querySelector('.contact-form input[type="text"]');
  const emailInput   = document.querySelector('.contact-form input[type="email"]');
  const msgTextarea  = document.querySelector('.contact-form textarea');
  const sendBtn      = document.querySelector('.contact-form button[type="submit"]');
  const chatInputEl  = document.getElementById('chatInput');

  if (nameInput)   nameInput.placeholder   = t['contact.namePlaceholder'];
  if (emailInput)  emailInput.placeholder  = t['contact.emailPlaceholder'];
  if (msgTextarea) msgTextarea.placeholder = t['contact.messagePlaceholder'];
  if (sendBtn)     sendBtn.innerHTML       = `${t['contact.send']} <i class="fas fa-paper-plane"></i>`;
  if (chatInputEl && t['chat.placeholder']) chatInputEl.placeholder = t['chat.placeholder'];

  // Update typewriter phrases
  if (window.updateTypewriterPhrases) {
    window.updateTypewriterPhrases(typewriterPhrases[lang]);
  }

  // Update chatbot language
  if (window.updateChatLanguage) {
    window.updateChatLanguage(lang);
  }

  // Update active button
  document.querySelectorAll('.lang-option').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  // Update label
  const labels = { en: 'EN', fr: 'FR', rw: 'RW' };
  document.getElementById('currentLang').textContent = labels[lang];

  // Save preference
  localStorage.setItem('lang', lang);
}

// ===== LANG SWITCHER TOGGLE =====
const langSwitcher = document.getElementById('langSwitcher');
const langBtn      = document.getElementById('langBtn');
const langDropdown = document.getElementById('langDropdown');

langBtn.addEventListener('click', (e) => {
  e.stopPropagation();
  langSwitcher.classList.toggle('open');
});

document.addEventListener('click', () => langSwitcher.classList.remove('open'));

document.querySelectorAll('.lang-option').forEach(btn => {
  btn.addEventListener('click', () => {
    applyTranslation(btn.getAttribute('data-lang'));
    langSwitcher.classList.remove('open');
  });
});

// Load saved language on page load
const savedLang = localStorage.getItem('lang') || 'en';
applyTranslation(savedLang);
